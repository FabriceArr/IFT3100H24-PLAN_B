void Skybox::loadCubemap(std::string path)
{
    ofImage img;
    img.loadImage(path);
    cubemap.load(path, 512);
}

Skybox::Skybox()
{
    
}

void Skybox::setup()
{

    skybox.setupShaderFromFile(GL_VERTEX_SHADER, "skybox/Shaders/skyboxy.vert");
    skybox.setupShaderFromFile(GL_FRAGMENT_SHADER, "skybox/Shaders/skyboxy.frag");
    skybox.linkProgram();

  

    // setup cubemap
    loadCubemap("skybox/skybox.png");
}

void Skybox::draw() {

    glDepthFunc(GL_LEQUAL);

    skybox.begin();
    glActiveTexture(GL_TEXTURE0);
    cubemap.drawCubeMap();


    glDepthFunc(GL_LESS);
    skybox.end();
}


